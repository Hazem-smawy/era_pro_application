enum AppRouteName {
  auth,
  home,
  bnp,
  companyInfo,
  salse,
  curency,
  store,
}

extension AppRouteExtension on AppRouteName {
  String get name {
    switch (this) {
      case AppRouteName.auth:
        return "/auth";
      case AppRouteName.home:
        return "/home";
      case AppRouteName.bnp:
        return '/bnp';
      case AppRouteName.companyInfo:
        return '/companyInfo';
      case AppRouteName.salse:
        return '/salse';
      case AppRouteName.curency:
        return '/crency';
      case AppRouteName.store:
        return '/store';
      default:
        return "/";
    }
  }
}
